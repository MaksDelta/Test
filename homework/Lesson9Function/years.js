function howOldAreYou(age) {

    if (age > 0) {
        return 'Твій час ше не прийшов';
    } else if (age >= 18) {
        return 'В тебе ше все по переду'
    } else if (age < 18) {
        return 'Твій час настав'
    } else if (age < 99) {
        return 'Тобі залишилось не довго'
    } else (age < 120) 
        return 'Іншим разом'
}
